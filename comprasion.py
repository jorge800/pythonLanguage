# Python Comprasion Operator

x = 5 # Assignment Operator
y = 72 # Assignment Operator
print("Value of x is "+str(x)+" and value of y is "+str(y)+".")
z = (x == y)
print("Value of z: "+str(z)+".")
z = (x != y)
print("Value of z: "+str(z)+".")
z = (x > y)
print("Value of z: "+str(z)+".")
z = (x < y)
print("Value of z: "+str(z)+".")
z = (x >= y)
print("Value of z: "+str(z)+".")
z = (x <= y)
print("Value of z: "+str(z)+".")
