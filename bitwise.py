# Python Bitwise Operators

x = 6
y = 2
z = x & y;
print("Value of variable 'z' is "+str(z)+".")
z = x | y;
print("Value of variable 'z' is "+str(z)+".")
z = x ^ y;
print("Value of variable 'z' is "+str(z)+".")
# z = x ! y;
# print("Value of variable 'z' is "+str(z)+".")
z = x << y;
print("Value of variable 'z' is "+str(z)+".")
z = x >> y;
print("Value of variable 'z' is "+str(z)+".")
